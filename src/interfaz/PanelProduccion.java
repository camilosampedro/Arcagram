    public Produccion getProduccion() {
        Produccion produccion = new Produccion();
        produccion.setParteIzquierda(new NoTerminal(jtfNoTerminal.getText()));
        Conjunto definicion = new Conjunto();
        String definicionTexto = jtfDefinicion.getText();
        String noTerminal = "";
        boolean abierto = false;
        for (int i = 0; i < definicionTexto.length(); i++) {
            if (!abierto) {
                if (definicionTexto.charAt(i) == '<') {
                    abierto = true;
                    noTerminal = "";
                } else if (definicionTexto.charAt(i) == '>') {
                    JOptionPane.showMessageDialog(this, "Error", "Error en la producci贸n " + getNumero(), JOptionPane.ERROR_MESSAGE);
                    return null;
                } else {
                    definicion.agregar(definicionTexto.charAt(i));
                }
            } else {
                if (definicionTexto.charAt(i) == '>') {
                    abierto = false;
                    if (!"".equals(noTerminal)) {
                        definicion.agregar(new NoTerminal(noTerminal));
                    }
                } else if (definicionTexto.charAt(i) == '<') {
                    JOptionPane.showMessageDialog(this, "Error", "Error en la producci贸n " + getNumero(), JOptionPane.ERROR_MESSAGE);
                    return null;
                } else {
                    noTerminal = noTerminal + definicionTexto.charAt(i);
                }
            }
        }
        produccion.setDefinicion(definicion);
        return produccion;
    }

