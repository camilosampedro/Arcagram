    private void btnCargarGramaticaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarGramaticaActionPerformed
        ObjectInputStream entrada = null;
        File archivo;
        Gramatica gramatica = Gramatica.getInstance();
        try {
            // TODO add your handling code here:
            JFileChooser selector = new JFileChooser();
            selector.setFileFilter(new FileNameExtensionFilter("Archivos de gramaticas (*.gram)", "gram"));
            int resultado = selector.showOpenDialog(this);
            if (resultado == JFileChooser.APPROVE_OPTION) {
                archivo = selector.getSelectedFile();
                entrada = new ObjectInputStream(new FileInputStream(archivo));
                gramatica = (Gramatica) entrada.readObject();
                GUIGramatica guiGramatica = new GUIGramatica();
                guiGramatica.setArchivo(archivo);
                guiGramatica.setVisible(true);
                this.dispose();
            }
        } catch (IOException ex) {
            System.err.println("Error en la lectura de archivo.");
        } catch (ClassNotFoundException ex) {
            System.err.println("Error en la serializaci√≥n.");
        } finally {
            try {
                if (entrada != null) {
                    entrada.close();
                }
            } catch (IOException ex) {
                Logger.getLogger(GUIPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnCargarGramaticaActionPerformed

